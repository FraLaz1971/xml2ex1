/** 
The CDA is a specialized utility designed to bridge the gap between the scientific
post-processing of ExoMars Trace Gas Orbiter (TGO) data and its long-term
preservation in the ESA Planetary Science Archive (PSA). 

Rather than being a standalone data analysis tool, it serves as the final ”archival stage” of the
CaSSIS DTM production pipeline. 

The software operates as a transformation engine that ingests metadata from post-processing outputs 
(e.g., header information from GeoTIFF or PamDataSet metadata xml) and maps it to the PDS4
Information Model. 

It is constrained by the strict XML schema requirements
defined by the PDS4 Management Council and the specific mission-level meta-
data dictionaries of ExoMars 2016. CDA shall be able to operate on different
kind of input data array given the specific input configuration file defined as
explained in 2.1.8 System Context:
- Input: It consumes Digital Terrain Models (DTMs) and associated meta-
data generated by the CaSSIS stereo-reconstruction post-processing work-
flow. The metadata will be written in configuration files to be then put
in input to the CDA.
- Output: It produces valid PDS4 XML labels and organized data bundles
ready for ingestion into the ESA PSA via the PSA Data Upload System.
- Environmental Context: The software must interface with the ExoMars
2016 Local Data Dictionary (LDD) to ensure all discipline-specific at-
tributes (e.g., geometry, cartography) are compliant with the CaSSIS ex-
periment requirements.


* 
* The usage command line is:
* $ ./archivepds_cas 

usage:./archivepds_cas <file.conf> <nproducts> <prodname1> <prodname2> ... <prodnamen>

e.g: ./archivepds_cas arpds.conf 1 image.raw

*/
 
 
